
public class Calculator {

	public int add(int one, int two){
		return one + two;
	}
	public int subtract(int one, int two){
		return one - two;
	}
	public double divide(int one, int two){
		return (double) one/two;
	}
	public double mutiply(int one, int two){
		return (double) one * two;
	}
	
	

}
